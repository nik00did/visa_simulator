class Person {
    constructor(id, firstName, lastName, gender, age, health, bankAccount, hasPassport) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.gender = gender;
        this.age = age;
        this.health = health;
        this.bankAccount = bankAccount;
        this.hasPassport = hasPassport;
    }
}